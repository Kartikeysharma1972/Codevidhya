function gradeBand(grade) {
  if (grade === 'Kindergarten' || ['Grade 1','Grade 2','Grade 3','Grade 4','Grade 5'].includes(grade)) return 'primary'
  if (['Grade 6','Grade 7','Grade 8'].includes(grade)) return 'middle'
  if (['Grade 9','Grade 10'].includes(grade)) return 'secondary'
  if (['Grade 11','Grade 12'].includes(grade)) return 'senior'
  if (grade === 'College') return 'college'
  return 'middle'
}

export function findTopicDescription(grade, subject, topicLabel) {
  const subs = cbseTOC[grade]
  if (!subs || !subs[subject]) return ''
  const t = subs[subject].find(t => t.topic === topicLabel)
  return t ? t.description : ''
}
